package com.tianyu.pojo;

public class Hero {

	private int heroId;
	
	private String heroName;
	
	private String heroType;
	
	private String heroIntro;
	
	private String heroImagePath;

	public int getHeroId() {
		return heroId;
	}

	public void setHeroId(int heroId) {
		this.heroId = heroId;
	}

	public String getHeroName() {
		return heroName;
	}

	public void setHeroName(String heroName) {
		this.heroName = heroName;
	}

	public String getHeroType() {
		return heroType;
	}

	public void setHeroType(String heroType) {
		this.heroType = heroType;
	}

	public String getHeroIntro() {
		return heroIntro;
	}

	public void setHeroIntro(String heroIntro) {
		this.heroIntro = heroIntro;
	}

	public String getHeroImagePath() {
		return heroImagePath;
	}

	public void setHeroImagePath(String heroImagePath) {
		this.heroImagePath = heroImagePath;
	}
	
	
}
